# nested_tuple[0] refers to the first element of the tuple, which is the string "Tuple".
# nested_tuple[0][3] means accessing the 4th character (index 3) of the string "Tuple".

nested_tuple = ("Tuple",[4,5,6,7],[46,32,45,56])
print(nested_tuple[0][4])  # output: e
print(nested_tuple[1][3])  # output: 7
import numpy as np

# array() - Creates an array.
arr = np.array([1,2,3])
print(arr)


# arange() - create values in a range
arr1 = np.arange(1,10,2)
print(arr1)

#zero() - Create array filled with zeros
arr2 =np.zeros((2,3))
print(arr2)

#ones() = creates array filled with ones
arr3 = np.ones((2,3))
print(arr3)

# linespace() - create evenly spaced numbers
arr4 = np.linspace(1,5,5)
print(arr4)

#eye() - create identity matrix
arr5 = np.eye(4)
print(arr5)

